// Emits src/runtime/generated/nameToAlpha2.js — the country NAME -> ISO alpha-2
// map the flag lookup needs now that a region's owner is the country's name
// rather than its GADM code.
//
// Flags are keyed on alpha-2 (flagcdn.com URLs and regional-indicator emoji both
// derive from it), and the only route to alpha-2 used to be the GID_0 code. Once
// owner is "Russia" rather than "RUS", every flag call site hands this a name, so
// without this table every country on the map loses its flag.
//
// It imports the REAL gidToAlpha2 rather than reimplementing the lookup. That is
// deliberate: gidToAlpha2 consults TWO indirection tables — DISPUTED_TERRITORY_PARENT
// (Z01..Z09 -> claimant) and SPECIAL_TERRITORY_PARENT (XAD -> GBR, ZNC -> TUR) —
// and a reimplementation that forgot the second would silently drop Akrotiri and
// Dhekelia and Northern Cyprus. Importing it means this table cannot drift from
// the thing it mirrors.
//
// Run whenever server/country-names.json changes. The output IS committed — a
// fresh clone must build without running this first, and an import of a
// gitignored generated file is exactly how "Could not resolve
// './generated/fallbackColors.js'" reached users once already.
import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import path from "node:path";
import url from "node:url";

const ROOT = path.resolve(path.dirname(url.fileURLToPath(import.meta.url)), "..");
const SRC = path.join(ROOT, "server", "country-names.json");
const OUT_DIR = path.join(ROOT, "src", "runtime", "generated");
const OUT = path.join(OUT_DIR, "nameToAlpha2.js");

// Bootstrap: countryFlags.js imports the very table we are about to write, so it
// has to exist before we can import countryFlags to build it. Stub it if missing.
// Generating against an empty stub is still correct — the name path just misses
// and every lookup falls through to the code path, which is all this needs.
if (!existsSync(OUT)) {
  mkdirSync(OUT_DIR, { recursive: true });
  writeFileSync(OUT, "/* AUTO-GENERATED — bootstrap stub. */\nexport default {};\n", "utf8");
}
const { gidToAlpha2 } = await import(url.pathToFileURL(path.join(ROOT, "src", "runtime", "countryFlags.js")).href);

const registry = JSON.parse(readFileSync(SRC, "utf8"));

// name -> alpha2, plus every code that produced it, so a conflict can be reported
// with the codes that caused it rather than just a count.
const byName = new Map();
for (const [code, name] of Object.entries(registry)) {
  const key = String(name || "").trim().toLowerCase();
  if (!key) continue;
  const alpha2 = gidToAlpha2(code);
  if (!alpha2) continue; // e.g. XCA "Caspian Sea" has no flag — it has none today either
  const entry = byName.get(key) ?? { alpha2s: new Set(), codes: [] };
  entry.alpha2s.add(alpha2);
  entry.codes.push(code);
  byName.set(key, entry);
}

// A name that resolves to two different flags would make this table a coin flip.
// It cannot happen today — the only names shared by several codes are India,
// China and Pakistan, whose disputed Z0x codes all resolve to the claimant's own
// alpha2 — but assert it rather than trust it, so a future country-names.json
// change fails the build instead of shipping the wrong flag.
const conflicts = [...byName.entries()].filter(([, e]) => e.alpha2s.size > 1);
if (conflicts.length) {
  for (const [name, e] of conflicts) {
    console.error(`  ${name}: ${[...e.alpha2s].join(", ")} (from ${e.codes.join(", ")})`);
  }
  throw new Error(`${conflicts.length} country name(s) resolve to more than one flag — see above.`);
}

const table = {};
for (const [key, e] of [...byName.entries()].sort(([a], [b]) => a.localeCompare(b))) {
  table[key] = [...e.alpha2s][0];
}

mkdirSync(OUT_DIR, { recursive: true });
writeFileSync(
  OUT,
  `/* AUTO-GENERATED by scripts/generate-name-to-alpha2.mjs — do not edit. */\n`
    + `// Country name (lowercased) -> ISO 3166-1 alpha-2. See the generator for why\n`
    + `// this is derived from country-names.json rather than hand-written.\n`
    + `export default ${JSON.stringify(table, null, 2)};\n`,
  "utf8",
);

const merged = [...byName.values()].filter((e) => e.codes.length > 1).length;
console.log(
  `nameToAlpha2.js: ${Object.keys(table).length} names from ${Object.keys(registry).length} codes`
    + ` (${merged} name(s) shared by several codes, each resolving to one flag) → ${path.relative(ROOT, OUT)}`,
);
